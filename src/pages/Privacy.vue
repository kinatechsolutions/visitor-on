            as a web beacon) is a small string of code that represents a clear graphic image and is
            used in conjunction with a cookie. In contrast to cookies, which are stored on a user's
            computer hard drive, pixel tags are embedded invisibly on web pages or in emails. A
            pixel tag allows us to capture certain additional types of information about a visitor's
            actions on a web site, such as a visitor's cookie number, the time, date, duration and
            number of page views, a description of the page where the tag is placed and details
            about any items that were purchased. Pixel tags help us analyse our customers' online
            behaviour and measure the effectiveness of our website and our advertising. We work with
            service providers that help us track, collect and analyse this information.
          </p>
